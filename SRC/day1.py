# collections [list,tuple,dictionary,set]

# list

q1 = ['pune','delhi','mumbai','banglore']
q2 = [11,22,33,44]
print(q1)
print(q2)
print(type(q1))
print(type(q2))

# tuple (fixed length)
q3 = ("ram","sham","satish","sachin")
print(q3)
print(type(q3))


# set (does not allow duplicate values)

q4 = {"chinmay","sarika","poorva","ram","chinmay"}
print(q4)
print(type(q4))


# dictionary
q5 = {
    'firstname' : 'saurabh',
    'lastname' : 'mahajan',
    "age": 29,
    "rollNo": 88
}
print(q5)
print(type(q5))